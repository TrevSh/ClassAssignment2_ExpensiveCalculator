#include <iostream>

int main()
{
    std::cout << "7 + 3 = " << 7 + 3 << '\n';
    std::cout << "7 - 3 = " << 7 - 3 << '\n';
    std::cout << "7 * 3 = " << 7 * 3 << '\n';

    std::cout << "7 / 3 = " << 7 / 3 << '\n';
    std::cout << "7.0 / 3.0 = " << 7.0 / 3.0 << '\n';

    std::cout << "7 % 3 = " << 7 % 3 << '\n';

    std::cout << "7 + 3 * 5 = " << 7 + 3 * 5 << '\n';
    std::cout << "(7 + 3) * 5 = " << (7 + 3) * 5 << '\n';

    return 0;
}
